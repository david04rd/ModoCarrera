    private Equipo devolverEquipo(String nombreEquipo) {

        Equipo equipo=null;

        LinkedList<Jugador> jugadoresEquipo = new LinkedList<Jugador>();
        int idEquipo=0;
        int annioFundacion;
        int presupuesto;
        Entrenador entrenador=null;

        PreparedStatement st;
        
        try {
            st = con.prepareStatement("select * from equipo e where nombre=?");

            st.setString(1, nombreEquipo);

            ResultSet rs = st.executeQuery();

            while(rs.next()){
                idEquipo=rs.getInt("idEquipo");
                presupuesto=rs.getInt("presupuesto");
                annioFundacion=rs.getInt("annoFundacion");
                jugadoresEquipo=cargarJugadores(idEquipo);
                entrenador=cargaEntrenador(idEquipo);
                equipo=new Equipo(idEquipo, nombreEquipo, annioFundacion, presupuesto, jugadoresEquipo, entrenador);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Frame_Principal.class.getName()).log(Level.SEVERE, null, ex);
        }

        return equipo;
    }
    
