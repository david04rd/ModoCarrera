     private LinkedList<Equipo> cargarEquipos() {
        equipos = new LinkedList<Equipo>();
	LinkedList<Jugador> jugadoresEquipo = new LinkedList<Jugador>();
        int idEquipo=0;
	String nombreEquipo;
	int annioFundacion;
	int presupuesto;
	Entrenador e;
        
        PreparedStatement st;
        
        try {
            st = con.prepareStatement("select * from equipo e ");
  
            ResultSet rs = st.executeQuery();
                    
            while(rs.next()){
                idEquipo=rs.getInt("idEquipo");
                nombreEquipo=rs.getString("nombre");
                presupuesto=rs.getInt("presupuesto");
                annioFundacion=rs.getInt("annoFundacion");
                jugadoresEquipo=cargarJugadores(idEquipo);
                e=cargaEntrenador(idEquipo);
                equipos.add(new Equipo(idEquipo, nombreEquipo, annioFundacion, presupuesto, jugadoresEquipo, e));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Frame_Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
 
        return equipos;
    }
    
