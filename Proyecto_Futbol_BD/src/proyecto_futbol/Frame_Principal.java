    private LinkedList<Jugador> cargarJugadores(int idEquipo) {
        
        LinkedList<Jugador> jugadoresEquipo = new LinkedList<Jugador>();
        
        Jugador j;
        
        String nombre, nacionalidad, posicion;
	int edad, valoracionGeneral, precio, idJugador;
        
        PreparedStatement st;
        
        try {
            st = con.prepareStatement("select * from jugador j where idEquipo=?");
            
            st.setInt(1,idEquipo);
            
            ResultSet rs = st.executeQuery();
                    
            while(rs.next()){
                idJugador=rs.getInt("idJugador");
                nombre=rs.getString("nombre");
                posicion=rs.getString("posicion");
                nacionalidad=rs.getString("nacionalidad");
                edad=rs.getInt("edad");
                valoracionGeneral=rs.getInt("valoracionGeneral");
                precio=rs.getInt("precio");

                j = new Jugador(idJugador, nombre, posicion, nacionalidad, edad, valoracionGeneral, precio);
                
                jugadoresEquipo.add(j);
            }
        } catch (Exception ex) {
            Logger.getLogger(Frame_Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return jugadoresEquipo;
    }
    
